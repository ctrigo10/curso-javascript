// Coerción - Javascript a veces convierte tipos "automáticamente"

// Suma (string + número)
const sum = '5' + 2; // "52"

// Resta (string - número)
const rest = '5' - 2; // 3

// Booleanos a números
const myTrue = true + 1; // 2
const myFalse = false + 1; // 1

console.log('sum', sum);
console.log('rest', rest);
console.log('myTrue', myTrue);
console.log('myFalse', myFalse);
